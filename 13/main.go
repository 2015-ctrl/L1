package main

import "fmt"

func main() {
	a := 1 // бинарно: 01
	b := 2 // бинарно: 10

	// Шаг 1: a = a ^ b
	// 01 ^ 10 = 11 (в десятичной: 3)
	a = a ^ b // теперь a = 3, b = 2

	// Шаг 2: b = a ^ b
	// сейчас a = 3 (11₂), b = 2 (10₂)
	// 11 ^ 10 = 01 (в десятичной: 1)
	b = a ^ b // теперь a = 3, b = 1

	// Шаг 3: a = a ^ b
	// сейчас a = 3 (11₂), b = 1 (01₂)
	// 11 ^ 01 = 10 (в десятичной: 2)
	a = a ^ b // теперь a = 2, b = 1

	fmt.Println(a, b)
}
